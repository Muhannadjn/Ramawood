// GPS Navigation function
function openLocation() {
  const address = "Gåsagången 19, 422 61 Hisings Backa, Sweden";
  const encodedAddress = encodeURIComponent(address);
  
  // Check if user is on mobile device
  const isMobile = /Android|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
  
  if (isMobile) {
    // Try to open native maps app first
    if (/iPhone|iPad|iPod/i.test(navigator.userAgent)) {
      // iOS - try Apple Maps first, fallback to Google Maps
      window.open(`maps://maps.apple.com/?q=${encodedAddress}`, '_blank');
      setTimeout(() => {
        window.open(`https://maps.google.com/maps?q=${encodedAddress}`, '_blank');
      }, 1000);
    } else if (/Android/i.test(navigator.userAgent)) {
      // Android - try Google Maps app first
      window.open(`intent://maps.google.com/maps?q=${encodedAddress}#Intent;scheme=https;package=com.google.android.apps.maps;end`, '_blank');
      setTimeout(() => {
        window.open(`https://maps.google.com/maps?q=${encodedAddress}`, '_blank');
      }, 1000);
    } else {
      // Other mobile devices
      window.open(`https://maps.google.com/maps?q=${encodedAddress}`, '_blank');
    }
  } else {
    // Desktop - open Google Maps in browser
    window.open(`https://maps.google.com/maps?q=${encodedAddress}`, '_blank');
  }
}

