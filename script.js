// Scroll effects
function initializeScrollEffects() {
  let ticking = false;

  function updateScrollEffects() {
    const scrolled = window.pageYOffset;
    const rate = scrolled * -0.3;

    // Parallax effect for hero background
    const heroBackground = document.querySelector('.hero-bg-img');
    if (heroBackground) {
      heroBackground.style.transform = `translate3d(0, ${rate}px, 0)`;
    }

    // Update navbar active link based on scroll position
    updateActiveNavLink();

    ticking = false;
  }

  window.addEventListener('scroll', function() {
    if (!ticking) {
      requestAnimationFrame(updateScrollEffects);
      ticking = true;
    }
  });
}

// Update active navigation link based on scroll position
function updateActiveNavLink() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.nav-menu a[href^="#"]');

  let current = '';

  sections.forEach(section => {
    const sectionTop = section.offsetTop - 100;
    const sectionHeight = section.clientHeight;

    if (window.pageYOffset >= sectionTop && window.pageYOffset < sectionTop + sectionHeight) {
      current = section.getAttribute('id');
    }
  });

  navLinks.forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('href') === `#${current}`) {
      link.classList.add('active');
    }
  });
}

// Language switcher functionality
function initializeLanguageSwitcher() {
  const allLangButtons = document.querySelectorAll('.lang-btn');
  
  allLangButtons.forEach(button => {
    button.addEventListener('click', function() {
      const selectedLang = this.getAttribute('data-lang');
      if (selectedLang !== currentLang) {
        switchLanguage(selectedLang);
        updateActiveLangButton(selectedLang);
      }
    });
  });
}

function switchLanguage(lang) {
  currentLang = lang;

  // Update text content
  const elements = document.querySelectorAll('[data-translate]');
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    // Check main translations first, then form translations
    const translation = (translations[lang] && translations[lang][key]) || 
                       (formTranslations[lang] && formTranslations[lang][key]);
    if (translation) {
      element.textContent = translation;
    }
  });

  // Update HTML attributes
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Update page title
  const titles = {
    en: 'RamaWood - Trusted Swedish Wood Trading Company',
    ar: 'راما وود - شركة سويدية موثوقة لتجارة الأخشاب',
    sv: 'RamaWood - Pålitligt svenskt trähandelsföretag'
  };
  document.title = titles[lang] || titles.en;

  // Add smooth transition for RTL switch
  document.body.style.transition = 'all 0.3s ease';
  setTimeout(() => {
    document.body.style.transition = '';
  }, 300);
}

function updateActiveLangButton(selectedLang) {
  const allLangButtons = document.querySelectorAll('.lang-btn');
  
  allLangButtons.forEach(button => {
    button.classList.remove('active');
    if (button.getAttribute('data-lang') === selectedLang) {
      button.classList.add('active');
    }
  });
}

// Enhanced smooth scrolling with easing
function smoothScrollTo(target, duration = 800) {
  const targetPosition = target.offsetTop - 80;
  const startPosition = window.pageYOffset;
  const distance = targetPosition - startPosition;
  let startTime = null;

  function ease(t, b, c, d) {
    t /= d / 2;
    if (t < 1) return c / 2 * t * t + b;
    t--;
    return -c / 2 * (t * (t - 2) - 1) + b;
  }

  function animation(currentTime) {
    if (startTime === null) startTime = currentTime;
    const timeElapsed = currentTime - startTime;
    const run = ease(timeElapsed, startPosition, distance, duration);
    window.scrollTo(0, run);
    if (timeElapsed < duration) requestAnimationFrame(animation);
  }

  requestAnimationFrame(animation);
}

// Utility functions
function debounce(func, wait) {
  let timeout;
  return function executedFunction(...args) {
    const later = () => {
      clearTimeout(timeout);
      func(...args);
    };
    clearTimeout(timeout);
    timeout = setTimeout(later, wait);
  };
}

// Performance optimizations
window.addEventListener('load', function() {
  // Remove loading states, add loaded states
  document.body.classList.add('loaded');

  // Lazy load images that are not in viewport
  const lazyImages = document.querySelectorAll('img[data-src]');
  const imageObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const img = entry.target;
        img.src = img.dataset.src;
        img.classList.remove('lazy');
        imageObserver.unobserve(img);
      }
    });
  });

  lazyImages.forEach(img => imageObserver.observe(img));
});

