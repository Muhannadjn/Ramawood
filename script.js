// Navigation functionality
function initializeNavigation() {
  // Smooth scrolling for navigation links
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      e.preventDefault();
      const target = document.querySelector(this.getAttribute('href'));
      if (target) {
        const offsetTop = target.offsetTop - 80;
        window.scrollTo({
          top: offsetTop,
          behavior: 'smooth'
        });

        // Close mobile menu if open
        navMenu.classList.remove('active');
        hamburger.classList.remove('active');
      }
    });
  });

  // Navbar scroll effect
  window.addEventListener('scroll', function() {
    if (window.scrollY > 100) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  });
}

// Mobile menu functionality
function initializeMobileMenu() {
  hamburger.addEventListener('click', function() {
    navMenu.classList.toggle('active');
    hamburger.classList.toggle('active');
    
    // Add body scroll lock when menu is open
    if (navMenu.classList.contains('active')) {
      document.body.style.overflow = 'hidden';
    } else {
      document.body.style.overflow = '';
    }
  });

  // Close mobile menu when clicking outside
  document.addEventListener('click', function(e) {
    if (!navMenu.contains(e.target) && !hamburger.contains(e.target)) {
      navMenu.classList.remove('active');
      hamburger.classList.remove('active');
      document.body.style.overflow = '';
    }
  });

  // Close mobile menu when clicking on nav links
  navMenu.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', function() {
      navMenu.classList.remove('active');
      hamburger.classList.remove('active');
      document.body.style.overflow = '';
    });
  });

  // Close menu on escape key
  document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape' && navMenu.classList.contains('active')) {
      navMenu.classList.remove('active');
      hamburger.classList.remove('active');
      document.body.style.overflow = '';
    }
  });
}

// Image loading functionality
function initializeImageLoading() {
  const images = document.querySelectorAll('img');

  images.forEach(img => {
    // Don't animate hero background and logo images
    if (img.classList.contains('hero-bg-img') || 
        img.classList.contains('logo-img') || 
        img.classList.contains('wood-img') ||
        img.classList.contains('about-furniture-img') ||
        img.classList.contains('team-img-rect')) {
      img.style.opacity = '1';
      return;
    }

    img.addEventListener('load', function() {
      this.style.opacity = '1';
    });

    img.addEventListener('error', function() {
      console.warn('Failed to load image:', this.src);
      this.style.opacity = '1'; // Show even if error
    });

    // Set initial opacity for other images
    img.style.opacity = '0';
    img.style.transition = 'opacity 0.3s ease';
    
    // Ensure images show after a short delay if load event doesn't fire
    setTimeout(() => {
      if (this.style.opacity === '0') {
        this.style.opacity = '1';
      }
    }, 1000);
  });
}

// Animation functionality
function initializeAnimations() {
  // Intersection Observer for scroll animations
  const observerOptions = {
    threshold: 0.15,
    rootMargin: '0px 0px -20px 0px'
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = '1';
        entry.target.style.transform = 'translateY(0)';
        entry.target.classList.add('animate-in');

        // Trigger counter animation for impact numbers
        if (entry.target.classList.contains('impact-number')) {
          animateCounter(entry.target);
        }

        // Add stagger effect for product cards
        if (entry.target.classList.contains('product-card')) {
          const cards = document.querySelectorAll('.product-card');
          const index = Array.from(cards).indexOf(entry.target);
          entry.target.style.transitionDelay = `${index * 0.05}s`;
        }
      }
    });
  }, observerOptions);

  // Apply animations to elements (exclude images and image containers)
  const animateElements = document.querySelectorAll(
    '.section-title, .section-subtitle, .product-card, .impact-item, .contact-card, .team-member, .about-text, .hero-content h1, .hero-content p, .hero-cta'
  );

  animateElements.forEach((el, index) => {
    // Don't animate image containers that contain critical images
    if (el.querySelector('.hero-bg-img, .logo-img, .wood-img, .about-furniture-img, .team-img-rect')) {
      return;
    }
    
    el.style.opacity = '0';
    el.style.transform = 'translateY(20px)';
    el.style.transition = `all 0.4s cubic-bezier(0.4, 0, 0.2, 1) ${index * 0.02}s`;
    observer.observe(el);
  });
}

// Counter animation for impact numbers
function animateCounter(element) {
  if (element.classList.contains('animated')) return;

  const target = parseInt(element.getAttribute('data-count'));
  const duration = 2000;
  const step = target / (duration / 16);
  let current = 0;

  element.classList.add('animated');

  const timer = setInterval(() => {
    current += step;
    if (current >= target) {
      current = target;
      clearInterval(timer);
    }
    element.textContent = Math.floor(current);
  }, 16);
}

